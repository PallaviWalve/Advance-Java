package it.eduction.entity;

public class Course {

	int course_id;
	String course_name;
	String course_provider;
	int duration;
	int fees;
	
	
	public Course() {
		super();
	}


	public Course(int course_id, String course_name, String course_provider, int duration, int fees) {
		super();
		this.course_id = course_id;
		this.course_name = course_name;
		this.course_provider = course_provider;
		this.duration = duration;
		this.fees = fees;
	}


	public int getCourse_id() {
		return course_id;
	}


	public void setCourse_id(int course_id) {
		this.course_id = course_id;
	}


	public String getCourse_name() {
		return course_name;
	}


	public void setCourse_name(String course_name) {
		this.course_name = course_name;
	}


	public String getCourse_provider() {
		return course_provider;
	}


	public void setCourse_provider(String course_provider) {
		this.course_provider = course_provider;
	}


	public int getDuration() {
		return duration;
	}


	public void setDuration(int duration) {
		this.duration = duration;
	}


	public int getFees() {
		return fees;
	}


	public void setFees(int fees) {
		this.fees = fees;
	}


	@Override
	public String toString() {
		return "Course [course_id=" + course_id + ", course_name=" + course_name + ", course_provider="
				+ course_provider + ", duration=" + duration + ", fees=" + fees + "]";
	}
	
	
	
	
}
